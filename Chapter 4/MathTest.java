public class MathTest {

    public static void main(String[] args) {
  
        System.out.println("SQRT: " + Math.sqrt(123));

        System.out.println("SIN: " + Math.sin(Math.toRadians(210))); 
 
        System.out.println("COS: " + Math.cos(Math.toRadians(150))); 
        System.out.println("FLOOR: " + Math.floor(28.7));

        System.out.println("CEIL: " + Math.ceil(22.7));

        System.out.println("ROUND: " + Math.round(22.7));

        System.out.println("MAX: " + Math.max(68, 71));

        System.out.println("MIN: " + Math.min(68, 71));

        System.out.println("RANDOM: " + Math.random());
    }
}